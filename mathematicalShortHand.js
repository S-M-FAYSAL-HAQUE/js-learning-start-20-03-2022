// addition shorthand
var price = 20;
price = price + 1;
price += 1;
price ++; //increment
console.log(price);

// subtraction shorthand
var love = 100;
love = love - 1;
love -= 1;
love --; //decrement
console.log(love);

// multiplication shorthand
var weight = 40;
// weight = weight * 2;
weight *= 2; // weight = weight * 2;
console.log(weight);

// division shorthand
var mainPrice = 100;
var offerPrice = 2;
var totalPrice = mainPrice / offerPrice;
mainPrice /= offerPrice; // mainPrice = mainPrice / offerPrice;
console.log(totalPrice);
console.log(mainPrice);

// exponentiation shorthand
var a = 5;
var b = 2;
var z = a ** b;
a **= b; // a = a ** b;
console.log(z);
console.log(a);
