// ***--- how to write a numeric variable ---***

// numeric variable likhte 5 ta jinish lage:
// 1. variable ar short form "var" likhte hoy.
// 2. variable ar ekta nam dite hoy. like, "price/age/weight/a/b/x/y" etc.
// 3. erpor equal "=" operator dite hoy.
// 4. operator ar pore kono variable ar "value" dite hoy. like, "var height = 10".
// 5. shob seshe ";" semicolon dite hoy. 

// ***--- how to write a string variable ---***

// string variable likhte 6 ta jinish lage:
// 1. variable ar short form "var" likhte hoy.
// 2. variable ar ekta nam dite hoy. like, "price/age/name/address/(any kind of name)/weight/a/b/x/y" etc.
// 3. erpor equal "=" operator dite hoy.
// 4. operator ar pore kono variable ar value/kono nam/character dite hoy. like, "var height = 10/fahad/india/@!lima" etc.
// 5. single/double quotation ("")/('') ar moddhe value/kono nam/character egula likhte hobe.
// 6. shob seshe ";" semicolon dite hoy.

// ***--- how to write a boolean variable ---***

// boolean variable likhte 5 ta jinish lage:
// 1. variable ar short form "var" likhte hoy.
// 2. variable ar ekta nam dite hoy. like, "price/age/weight/a/b/x/y/pass/subscribe" etc.
// 3. erpor equal "=" operator dite hoy.
// 4. operator ar pore kono variable ar "value" dite hoy. like, var height = true/false. true/false k kono quotation ar moddhe rakha jabe na.
// 5. shob seshe ";" semicolon dite hoy.