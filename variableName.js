// JavaScript case sensitive.

// Good practice for writing variable name:
var name = "faysal";
var userName = "Fahad"; [camel_case]
var PIE = 3.1416 (universal_truth_ar_jonno_capital_letter_use_kora_jay)

// another way to write variable name but this is not good practice:
var user_address = "239/1, West Manikdi";
var food99 = "apple";
